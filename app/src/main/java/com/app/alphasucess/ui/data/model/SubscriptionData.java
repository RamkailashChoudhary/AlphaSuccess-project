package com.app.alphasucess.ui.data.model;

public class SubscriptionData {

    private String id;
    private String title;
    private String examname;
    private String price;
    private String homebannerurl;
    private String smallbannerurl;
    private String brief;
    private String description;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getExamname() {
        return examname;
    }

    public void setExamname(String examname) {
        this.examname = examname;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getHomebannerurl() {
        return homebannerurl;
    }

    public void setHomebannerurl(String homebannerurl) {
        this.homebannerurl = homebannerurl;
    }

    public String getSmallbannerurl() {
        return smallbannerurl;
    }

    public void setSmallbannerurl(String smallbannerurl) {
        this.smallbannerurl = smallbannerurl;
    }

    public String getBrief() {
        return brief;
    }

    public void setBrief(String brief) {
        this.brief = brief;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
}
