package com.app.alphasucess.ui.tabui.ebook.adapters;

public class EbookData {

    private String id;
    private String bookname;
    private String imageurl;
    private String bookprice;
    private String offerallowed;
    private String offerrate;
    private String adddate;
    private String image;
    private boolean trash;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getBookname() {
        return bookname;
    }

    public void setBookname(String bookname) {
        this.bookname = bookname;
    }

    public String getImageurl() {
        return imageurl;
    }

    public void setImageurl(String imageurl) {
        this.imageurl = imageurl;
    }

    public String getBookprice() {
        return bookprice;
    }

    public void setBookprice(String bookprice) {
        this.bookprice = bookprice;
    }

    public String getOfferallowed() {
        return offerallowed;
    }

    public void setOfferallowed(String offerallowed) {
        this.offerallowed = offerallowed;
    }

    public String getOfferrate() {
        return offerrate;
    }

    public void setOfferrate(String offerrate) {
        this.offerrate = offerrate;
    }

    public String getAdddate() {
        return adddate;
    }

    public void setAdddate(String adddate) {
        this.adddate = adddate;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public boolean isTrash() {
        return trash;
    }

    public void setTrash(boolean trash) {
        this.trash = trash;
    }
}
